# Método startsWith string: Retorna si el valor al inicio coincide con la consulta.

texto = "Ejemplo super chafa."
print(texto.startswith("Ejemplo"))
print(texto.startswith("chafa"))

# Método endsWith string: Retorna si el valor al final coincide con la consulta.

texto = "Ejemplo super chafa."
print(texto.endswith("Ejemplo"))
print(texto.endswith("chafa."))